                            Esta aplicación tiene como objetivo en primer lugar, aplicar el test del modelo de estilos de aprendizaje propuesto por Felder y Silverman, el cual consta de 4 dimensiones de aprendizaje, cada una compuesta por dos estilos de aprendizaje; Activo-reflexivo, Sensitivo-intuitivo, Visual-verbal, Secuencia-global. Este test permite identificar la preferencia del estudiante hacia un estilo de aprendizaje de cada dimensión; Esta preferencia puede ser fuerte, media o equilibrada.
                            <br>
                            En segundo lugar, aplicar el test del modelo de “perfiles de jugadores”, propuesto por Andrzej Marczewski en ambientes de gamificación. El cual consta de una clasificación de 6 perfiles de jugadores; Jugadores (players), Triunfadores (achievers), Socializadores (socialisers), Filántropos (philanthropists), Espíritu Libre (free spirit) y Disruptores (disruptive). Con este test se busca identificar el tipo de jugador que describe las motivaciones de un estudiante para divertirse en un contexto de juego.
