                                    <dl>
                                        <dt>Sensitivos</dt>
                                        <dd> No les gusta aprender cosas que no vean útiles para el mundo real, son bastante prácticos. Están orientados hacia hechos, siempre desde una perspectiva concreta y dotados de una memoria ágil. Prestan atención a los detalles. </dd>

                                        <dt>Intuitivos</dt>
                                        <dd> Conceptuales por naturaleza. Comprenden rápidamente nuevos conceptos y trabajan bien con abstracciones y formulaciones matemáticas. Al contrario que los sensitivos, ellos están más orientados hacia las teorías. Les gusta innovar y no les gusta la monotonía, la repetición y la memorización excesiva. </dd>
                                    </dl>  
